function changeLanguage(lang) {
    // 1. Simpan preferensi bahasa ke memori browser
    localStorage.setItem('preferredLang', lang);

    // 2. Terjemahkan HANYA elemen yang memiliki tag data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // 3. Ubah warna tombol ID / EN yang sedang aktif
    const btnId = document.getElementById('btn-id');
    const btnEn = document.getElementById('btn-en');
    if (btnId) btnId.classList.toggle('lang-active', lang === 'id');
    if (btnEn) btnEn.classList.toggle('lang-active', lang === 'en');

    // 4. Trigger khusus untuk script JavaScript Dinamis (Divisi & Admin)
    if (typeof window.updateLocalContent === 'function') {
        window.updateLocalContent(lang);
    }
}

// Jalankan otomatis saat halaman pertama kali dibuka
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || 'id';
    changeLanguage(savedLang);
});